def no_mutable(a: int) -> int:
    a += 1
    print(f'In func {a = }') # –î–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏ —Ä–∞–±–æ—Ç—ã, –Ω–æ –Ω–µ
# –¥–ª—è –ø—Ä–∏–≤—ã—á–∫–∏ –ø—Ä–∏–Ω—Ç–∏—Ç—å –∏–∑ —Ñ—É–Ω–∫—Ü–∏–∏
    return a



a = 42
print(f'In main {a = }')
z = no_mutable(a)
print(f'{a = }\t{z = }')


# —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Å–∏–º–≤–æ–ª—ã –∑–∞–¥–∞–Ω–∏—è —Ñ–æ—Ä–º–∞—Ç–∞ –¥–∞–ª–µ–µ.
# ‚óè :.2f ‚Äî —á–∏—Å–ª–æ –ø–∏ –≤—ã–≤–æ–¥–∏–º —Å —Ç–æ—á–Ω–æ—Å—Ç—å –¥–≤–∞ –∑–Ω–∞–∫–∞ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π
# ‚óè :>10 ‚Äî —ç–ª–µ–º–µ–Ω—Ç—ã —Å–ø–∏—Å–∫–∞ –≤—ã–≤–æ–¥—è—Ç—Å—è —Å –≤—ã—Ä–∞–≤–Ω–∏–≤–∞–Ω–∏–µ–º –ø–æ –ø—Ä–∞–≤–æ–º—É –∫—Ä–∞—é –∏
# –æ–±—â–µ–π —à–∏—Ä–∏–Ω–æ–π –≤—ã–≤–æ–¥–∞ –≤ 10 —Å–∏–º–≤–æ–ª–æ–≤
# ‚óè = ‚Äî –≤—ã–≤–æ–¥–∏–º –∏–º—è –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π, –∑–Ω–∞–∫ —Ä–∞–≤–µ–Ω—Å—Ç–≤–∞ —Å –ø—Ä–æ–±–µ–ª–∞–º–∏ –¥–æ –∏ –ø–æ—Å–ª–µ –Ω–µ–≥–æ –∏
# —Ç–æ–ª—å–∫–æ –ø–æ—Ç–æ–º –∑–Ω–∞—á–µ–Ω–∏–µ.
# ‚óè :_ ‚Äî —á–∏—Å–ª–æ —Ä–∞–∑–¥–µ–ª—è–µ—Ç—Å—è —Å–∏–º–≤–æ–ª–æ–º –ø–æ–¥—á—ë—Ä–∫–∏–≤–∞–Ω–∏—è –¥–ª—è –¥–µ–ª–µ–Ω–∏—è –Ω–∞ –±–ª–æ–∫–∏ –ø–æ 3

# –†–∞–∑–±–∏—Ä–∞—Ç—å –∏—Ö –≤—Å–µ –Ω–µ—Ç —Å–º—ã—Å–ª–∞. –†–∞—Å—Å–º–æ—Ç—Ä–∏–º —Ç—Ä–∏ ‚Äú–≥–ª–∞–≤–Ω—ã—Ö‚Äù –Ω–∞ –ø—Ä–∏–º–µ—Ä–∞—Ö.

# –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —á–µ—Ä–µ–∑ %
# –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Å–∏–º–≤–æ–ª–∞ % —è–≤–ª—è–µ—Ç—Å—è —Å—Ç–∞—Ä—ã–º —Å–ø–æ—Å–æ–±–æ–º —É–∫–∞–∑–∞–Ω–∏—è

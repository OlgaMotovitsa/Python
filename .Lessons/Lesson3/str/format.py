# –í —Å—Ç—Ä–æ–∫–µ —Ç–µ–∫—Å—Ç–∞ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –∑–Ω–∞–∫ % —Å —Å–∏–º–≤–æ–ª–æ–º —Ç–∏–ø–∞ –ø–æ—Å–ª–µ –Ω–µ–≥–æ. s ‚Äî —Å—Ç—Ä–æ–∫–∞, d ‚Äî
# —á–∏—Å–ª–æ –∏ —Ç.–ø. –ü–æ—Å–ª–µ —Å—Ç—Ä–æ–∫–∏ —É–∫–∞–∑—ã–≤–∞–µ—Ç—Å—è —Å–∏–º–≤–æ–ª % –∏ –ø–µ—Ä–µ—á–∏—Å–ª—è—é—Ç—Å—è –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ. –ï—Å–ª–∏
# –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –±–æ–ª—å—à–µ –æ–¥–Ω–æ–π, –æ–Ω–∏ –∑–∞–∫–ª—é—á–∞—é—Ç—Å—è –≤ –∫—Ä—É–≥–ª—ã–µ —Å–∫–æ–±–∫–∏ –∏ —Ä–∞–∑–¥–µ–ª—è—é—Ç—Å—è
# –∑–∞–ø—è—Ç–æ–π ‚Äî –ø–µ—Ä–µ–¥–∞—ë—Ç—Å—è –∫–æ—Ä—Ç–µ–∂.
# üî• –í–∞–∂–Ω–æ! –ü–æ–¥—Ä–æ–±–Ω–µ–µ –ø—Ä–æ –∏—Å–ø–æ–ª—å–∑—É–µ–º—ã –ª–∏—Ç–µ—Ä—ã –≤—ã –º–æ–∂–µ—Ç–µ –ø—Ä–æ—á–∏—Ç–∞—Ç—å –ø–æ
# —Å—Å—ã–ª–∫–µ –≤ –º–∞—Ç–µ—Ä–∏–∞–ª–∞—Ö –ª–µ–∫—Ü–∏–∏. –ü–æ–∫–∞ –∂–µ –¥–æ–≥–æ–≤–æ—Ä–∏–º—Å—è –Ω–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –¥–∞–Ω–Ω—ã–π
# —Å—Ç–∏–ª—å —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è, –µ—Å–ª–∏ —ç—Ç–æ –Ω–µ –æ–±—è–∑—ã–≤–∞–µ—Ç –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–π –º–æ–¥—É–ª—å.
# –ú–µ—Ç–æ–¥ format
# –ú–µ—Ç–æ–¥ —Ñ–æ—Ä–º–∞—Ç —è–≤–ª—è–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–æ–≤—ã–º –º–µ—Ç–æ–¥–æ–º –∏ –ø–æ–∑–≤–æ–ª—è–µ—Ç —Å–æ–µ–¥–∏–Ω—è—Ç—å –∑–∞—Ä–∞–Ω–µ–µ
# –∑–∞–≥–æ—Ç–æ–≤–ª–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç —Å –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–º–∏. –î–æ–ª–≥–æ–µ –≤—Ä–µ–º—è –±—ã–ª –æ—Å–Ω–æ–≤–Ω—ã–º —Å–ø–æ—Å–æ–±–æ–º
# —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è. –î–æ –≤–µ—Ä—Å–∏–∏ Python 3.6, –µ—Å–ª–∏ –±—ã—Ç—å —Ç–æ—á–Ω—ã–º.

name = 'Alex'
age = 12
text = '–ú–µ–Ω—è –∑–æ–≤—É—Ç {} –∏ –º–Ω–µ {} –ª–µ—Ç'.format(name, age)
print(text)

# –í —Å—Ç—Ä–æ–∫–µ –∏—Å–ø–æ–ª—å–∑—É—é—Ç—Å—è —Ñ–∏–≥—É—Ä–Ω—ã–µ —Å–∫–æ–±–∫–∏ –∫–∞–∫ –º–µ—Å—Ç–æ –¥–ª—è –ø–æ–¥—Å—Ç–∞–Ω–æ–≤–∫–∏ –∑–Ω–∞—á–µ–Ω–∏–π.
# –î–∞–ª–µ–µ –¥–ª—è —Å—Ç—Ä–æ–∫–∏ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –º–µ—Ç–æ–¥ format. –í –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –º–µ—Ç–æ–¥ –ø–æ–ª—É—á–∞–µ—Ç
# –Ω—É–∂–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö.

# f-—Å—Ç—Ä–æ–∫–∞
# –ù–∞—á–∏–Ω–∞—è —Å Python 3.7 –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è —Ç–µ–∫—Å—Ç–∞ –∏—Å–ø–æ–ª—å–∑—É—é—Ç f-—Å—Ç—Ä–æ–∫–∏. –û–Ω–∏
# —Ä–∞–±–æ—Ç–∞—é—Ç –±—ã—Å—Ç—Ä–µ–µ, —á–µ–º —Å—Ç–∞—Ä—ã–µ —Å–ø–æ—Å–æ–±—ã —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è. –ê –Ω–µ–∫–æ—Ç–æ—Ä—ã–µ
# —Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫–∏ —è–∑—ã–∫–∞ –ø—Ä–µ–¥–ª–∞–≥–∞—é—Ç —Å–¥–µ–ª–∞—Ç—å –∏—Ö —Å—Ç—Ä–æ–∫–∞–º–∏ –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –≤ –æ–¥–Ω–æ–º –∏–∑
# –±—É–¥—É—â–∏—Ö —Ä–µ–ª–∏–∑–æ–≤.
# f-—Å—Ç—Ä–æ–∫–∏ –ø–æ—Ö–æ–∂–∏ –Ω–∞ –±–æ–ª–µ–µ –∫–æ—Ä–æ—Ç–∫—É—é –∏ —á–∏—Ç–∞–µ–º—É—é –∑–∞–ø–∏—Å—å –º–µ—Ç–æ–¥–∞ —Ñ–æ—Ä–º–∞—Ç.

name = 'Alex'
age = 12
text = f'–ú–µ–Ω—è –∑–æ–≤—É—Ç {name} –∏ –º–Ω–µ {age} –ª–µ—Ç'
print(text)

# –ü–µ—Ä–µ–¥ –æ—Ç–∫—Ä—ã–≤–∞—é—â–µ–π –∫–∞–≤—ã—á–∫–æ–π –ø–∏—à—É—Ç f ‚Äî —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –æ—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—É—é —Å—Ç—Ä–æ–∫—É.
# –¢–µ–∫—Å—Ç –≤–Ω—É—Ç—Ä–∏ —Ñ–∏–≥—É—Ä–Ω—ã—Ö —Å–∫–æ–±–æ–∫ –≤–æ—Å–ø—Ä–∏–Ω–∏–º–∞–µ—Ç—Å—è –∫–∞–∫ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –∏ –Ω–∞ –ø–µ—á–∞—Ç—å
# –≤—ã–≤–æ–¥—è—Ç—Å—è –∏–∑ –∑–Ω–∞—á–µ–Ω–∏—è.
# üî• –í–∞–∂–Ω–æ! –î–ª—è –ø–µ—á–∞—Ç–∏ —Ñ–∏–≥—É—Ä–Ω—ã—Ö —Å–∫–æ–±–æ–∫ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥–≤–µ —Ñ–∏–≥—É—Ä–Ω—ã–µ —Å–∫–æ–±–∫–∏
# —Å–ª–∏—Ç–Ω–æ.

print(f'{{–§–∏–≥—É—Ä–Ω—ã–µ —Å–∫–æ–±–∫–∏}} –∏ {{name}}')

# –ü–æ–º–∏–º–æ –≤—ã–≤–æ–¥–∞ —Å–æ–¥–µ—Ä–∂–∏–º–æ–≥–æ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π –º–æ–∂–Ω–æ —É–∫–∞–∑–∞—Ç—å –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ
# —Å–∏–º–≤–æ–ª—ã, –≤–ª–∏—è—é—â–∏–µ –Ω–∞ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ.
# –£—Ç–æ—á–Ω–µ–Ω–∏–µ —Ñ–æ—Ä–º–∞—Ç–∞
# –°—É—â–µ—Å—Ç–≤—É—é —Ä–∞–∑–ª–∏—á–Ω—ã–µ —Å–ø–æ—Å–æ–±—ã —É—Ç–æ—á–Ω–µ–Ω–∏—è —Å–ø–æ—Å–æ–±–∞ –≤—ã–≤–æ–¥–∞ –∑–Ω–∞—á–µ–Ω–∏—è –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π.

pi = 3.1415
print(f'–ß–∏—Å–ª–æ –ü–∏ —Å —Ç–æ—á–Ω–æ—Å—Ç—å—é –¥–≤–∞ –∑–Ω–∞–∫–∞: {pi:.2f}') # –≤—ã–≤–µ—Å—Ç–∏ –ø–∏ —Å 2–º—è –∑–Ω–∞–∫–∞–º–∏ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π, f- float
# –ß–∏—Å–ª–æ –ü–∏ —Å —Ç–æ—á–Ω–æ—Å—Ç—å—é –¥–≤–∞ –∑–Ω–∞–∫–∞: 3.14

data = [3254, 4364314532, 43465474, 2342, 462256, 1747]
